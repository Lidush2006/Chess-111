CHECKMATE = 1000 # Значение мата (очень большое число)
STALEMATE = 0 # Значение пата
DEPTH = 3 # Глубина поиска (3 полухода)


def findBestMove(game_state, valid_moves, return_queue):
    global next_move
    next_move = None
    random.shuffle(valid_moves) # Перемешиваем ходы для разнообразия
    findMoveNegaMaxAlphaBeta(game_state, valid_moves, DEPTH, -CHECKMATE, CHECKMATE,
                             1 if game_state.white_to_move else -1)
    return_queue.put(next_move) # Перемешиваем ходы для разнообразия

#
def findMoveNegaMaxAlphaBeta(game_state, valid_moves, depth, alpha, beta, turn_multiplier):
    global next_move
    if depth == 0:
        return turn_multiplier * scoreBoard(game_state) # Оценка позиции

    max_score = -CHECKMATE
    for move in valid_moves:
        game_state.makeMove(move)# Делаем ход
        next_moves = game_state.getValidMoves()
        score = -findMoveNegaMaxAlphaBeta(game_state, next_moves, depth - 1, -beta, -alpha, -turn_multiplier)
        if score > max_score:
            max_score = score
            if depth == DEPTH: # Если это корневой уровень, сохраняем лучший ход
                next_move = move
        game_state.undoMove()# Отменяем ход
        # Альфа-бета отсечение
        if max_score > alpha:
            alpha = max_score
        if alpha >= beta:
            break # Отсекаем оставшиеся ходы
    return max_score

